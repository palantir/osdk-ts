// Action parameter and return-shape helpers. The full
// `client(MyAction).applyAction(args, options)` signature is composed in
// @osdk/client; from @osdk/api we pin the per-shape param helpers
// (`ActionParam.ObjectType` etc.), the options bag, and the two non-trivial
// return branches (edits-bearing and validation-only).

import type {
  ActionEditResponse,
  ActionParam,
  ActionValidationResponse,
  ApplyActionOptions,
  ApplyBatchActionOptions,
} from "../../actions/Actions.js";
import type { EmployeeApiTest } from "../../test/EmployeeApiTest.js";
import type { FooInterfaceApiTest } from "../../test/FooInterfaceApiTest.js";

// === Param shapes (what an action accepts for each parameter type) ===

/** What an object-typed action parameter accepts — primary key or full object identifiers. */
declare const probe_param_object: ActionParam.ObjectType<EmployeeApiTest>;

/** What an objectSet-typed action parameter accepts. */
declare const probe_param_objectSet: ActionParam.ObjectSetType<EmployeeApiTest>;

/** What an interface-typed action parameter accepts. */
declare const probe_param_interface: ActionParam.InterfaceType<
  FooInterfaceApiTest
>;

/** A primitive (string) action parameter. */
declare const probe_param_primitive_string: ActionParam.PrimitiveType<"string">;

/** A primitive (attachment) action parameter — non-trivial wire mapping. */
declare const probe_param_primitive_attachment: ActionParam.PrimitiveType<
  "attachment"
>;

/** A struct-typed action parameter — fields are mapped through DataValueClientToWire. */
declare const probe_param_struct: ActionParam.StructType<{
  name: "string";
  age: "integer";
  joined: "datetime";
}>;

/** The marker type for clearing a nullable parameter on an autogenerated modify action. */
declare const probe_param_null_value: ActionParam.NullValueType;

// === Options bag ===

/** The options accepted by client(action).applyAction(args, options). */
declare const probe_apply_options: ApplyActionOptions;

/** The options accepted by client(action).batchApplyAction(args, options). */
declare const probe_batch_apply_options: ApplyBatchActionOptions;

// === Return shapes ===

/** The edits-bearing payload returned when $returnEdits: true. */
declare const probe_action_edit_response: ActionEditResponse;

/** The validation-only response returned when $validateOnly: true. */
declare const probe_action_validation_response: ActionValidationResponse;
