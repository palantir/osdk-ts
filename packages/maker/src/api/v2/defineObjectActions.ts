// ---------------------------------------------------------------------------
// Overrides interfaces
// ---------------------------------------------------------------------------

/**
 * Optional overrides for auto-generated object action definitions.
 */
export interface ActionV2Overrides {
  apiName?: string;
  displayName?: string;
  status?: "active" | "experimental" | "example";
  excludedProperties?: string[];
}

/**
 * Overrides for create-or-modify actions, with an additional primaryKeyOption.
 */
export interface CreateOrModifyV2Overrides extends ActionV2Overrides {
  primaryKeyOption?: "autoGenerated" | "userInput";
}

// ---------------------------------------------------------------------------
// Runtime helpers
// ---------------------------------------------------------------------------

/** Check whether a PropertyV2Config describes a struct-typed property. */
function isStructProperty(prop: PropertyV2Config): boolean {
  if (typeof prop === "string") return false;
  const t = prop.type;
  return typeof t === "object" && t.type === "struct";
}

/**
 * Extract the action primitive type string from a PropertyV2Config.
 * Maps "date" to "datetime" (action parameters use "datetime", not "date").
 */
function propertyToActionType(
  prop: PropertyV2Config,
): string {
  const raw = typeof prop === "string" ? prop : (
    typeof prop.type === "string" ? prop.type : prop.type.type
  );
  if (raw === "date") return "datetime";
  return raw;
}

/**
 * Build the action parameter record from an ObjectV2Def's properties.
 *
 * - Always excludes struct properties.
 * - Optionally excludes the primary key.
 * - Optionally marks all params as nullable.
 * - Respects excludedProperties from overrides.
 */
function buildPropertyParams(
  objectDef: ObjectV2Def,
  opts: {
    excludePk: boolean;
    nullable: boolean;
    excludedProperties?: string[];
  },
): Record<string, ActionParameterV2Config> {
  const params: Record<string, ActionParameterV2Config> = {};
  const excluded = new Set(opts.excludedProperties ?? []);

  for (const [key, prop] of Object.entries(objectDef.properties)) {
    if (isStructProperty(prop)) continue;
    if (opts.excludePk && key === objectDef.primaryKeyPropertyApiName) continue;
    if (excluded.has(key)) continue;

    const actionType = propertyToActionType(prop);
    if (opts.nullable) {
      params[key] = {
        type: actionType,
        nullable: true,
      } as ActionParameterV2Config;
    } else {
      params[key] = actionType as ActionParameterV2Config;
    }
  }

  return params;
}

