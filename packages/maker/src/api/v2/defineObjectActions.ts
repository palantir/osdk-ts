/**
 * Map a PropertyV2Config to the corresponding action parameter type.
 * Returns ActionPrimitiveType to satisfy ActionParameterV2Config constraints.
 * Maps "date" -> "datetime" since actions use "datetime".
 */
type PropertyToActionPrimitive<P> = P extends "date" ? "datetime"
  : P extends ActionPrimitiveType ? P
  : P extends string ? ActionPrimitiveType
  : P extends { type: { type: "struct" } } ? never
  : P extends { type: "date" } ? "datetime"
  : P extends { type: infer T extends ActionPrimitiveType } ? T
  : P extends { type: infer _T extends string } ? ActionPrimitiveType
  : ActionPrimitiveType;

/** Extract keys from a properties record that are NOT struct-typed. */
type NonStructKeys<Props> =
  & {
    [K in keyof Props]: Props[K] extends { type: { type: "struct" } } ? never
      : K;
  }[keyof Props]
  & string;

// -- Create --

type CreateActionConfig<T extends ObjectV2Config> = {
  apiName: `create-object-${string}`;
  displayName: `Create ${T["displayName"]}`;
  parameters: {
    [K in NonStructKeys<T["properties"]>]: PropertyToActionPrimitive<
      T["properties"][K]
    >;
  };
  modifiedEntities: {
    [K in T["apiName"]]: { created: true; modified: false };
  };
};

