// Builds the markdown for the PR coverage comment from the merged
// `coverage/coverage-summary.json`. The CI job posts/updates a single comment
// via `gh` (no third-party action); the marker below is how it finds the
// existing comment to update in place.
//
// Snapshot only (current coverage, no base-branch diff).

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

// Display marker for the ✅/⚠️ column. Mirrors the per-package `thresholds`
// value baked into every vitest.config; the authoritative gate is vitest itself.
const THRESHOLD = 60;
const MARKER = "<!-- coverage-report -->";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..", "..");
const summaryPath = join(repoRoot, "coverage", "coverage-summary.json");
const outPath = join(repoRoot, "coverage", "pr-comment.md");

let summary;
try {
  summary = JSON.parse(readFileSync(summaryPath, "utf8"));
} catch {
  console.error(
    `Could not read ${summaryPath}. Run \`pnpm run coverage\` first.`,
  );
  process.exit(1);
}

// Aggregate per-file entries into per-package totals.
const byPkg = new Map();
for (const [file, m] of Object.entries(summary)) {
  if (file === "total") continue;
  const match = file.match(/packages\/([^/]+)\//);
  if (!match) continue;
  const pkg = `@osdk/${match[1]}`;
  const a = byPkg.get(pkg) ?? {
    lines: [0, 0],
    branches: [0, 0],
    functions: [0, 0],
    statements: [0, 0],
  };
  for (const k of ["lines", "branches", "functions", "statements"]) {
    a[k][0] += m[k].covered;
    a[k][1] += m[k].total;
  }
  byPkg.set(pkg, a);
}

// A 0/0 metric (nothing to cover) is treated as 100% so empty packages don't
// read as failing.
const pct = ([covered, total]) => (total === 0 ? 100 : (100 * covered) / total);
const fmt = (n) => `${n.toFixed(1)}%`;

const rows = [...byPkg.entries()]
  .map(([pkg, a]) => ({
    pkg,
    lines: pct(a.lines),
    branches: pct(a.branches),
    functions: pct(a.functions),
    statements: pct(a.statements),
  }))
  .sort((a, b) => a.lines - b.lines); // worst first

const under = rows.filter(
  (r) =>
    r.lines < THRESHOLD
    || r.branches < THRESHOLD
    || r.functions < THRESHOLD
    || r.statements < THRESHOLD,
);

const t = summary.total;
const lines = [];
lines.push(MARKER);
lines.push("## Coverage report");
lines.push("");
lines.push(
  `**Total: ${t.lines.pct}% lines** · ${t.branches.pct}% branches · `
    + `${t.functions.pct}% functions · ${t.statements.pct}% statements`,
);
lines.push("");
if (under.length === 0) {
  lines.push(
    `✅ All ${rows.length} packages meet the ${THRESHOLD}% threshold.`,
  );
} else {
  lines.push(
    `⚠️ **${under.length} of ${rows.length} package(s) below the `
      + `${THRESHOLD}% threshold** (warn-only, does not block the merge): `
      + under.map((r) => `\`${r.pkg}\``).join(", "),
  );
}
lines.push("");
lines.push(`<details><summary>Per-package coverage (${rows.length})</summary>`);
lines.push("");
lines.push("| Package | Lines | Branches | Functions | Statements | |");
lines.push("|---|--:|--:|--:|--:|:-:|");
for (const r of rows) {
  const ok = r.lines >= THRESHOLD && r.branches >= THRESHOLD
    && r.functions >= THRESHOLD && r.statements >= THRESHOLD;
  lines.push(
    `| ${r.pkg} | ${fmt(r.lines)} | ${fmt(r.branches)} | ${fmt(r.functions)} | `
      + `${fmt(r.statements)} | ${ok ? "✅" : "⚠️"} |`,
  );
}
lines.push("");
lines.push("</details>");
lines.push("");
lines.push(
  `<sub>Generated by the Coverage workflow. Barrel (\`index.ts\`), test, and `
    + `generated files are excluded.</sub>`,
);

const body = lines.join("\n") + "\n";
writeFileSync(outPath, body);
console.log(`Wrote ${outPath} (${under.length} package(s) under threshold)`);
